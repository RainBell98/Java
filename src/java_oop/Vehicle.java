package java_oop;

public class Vehicle {
	private String name;
	private int wheel;
	public Vehicle(String name, int wheel) {
		super();
		this.name = name;
		this.wheel = wheel;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getWheel() {
		return wheel;
	}
	public void setWheel(int wheel) {
		this.wheel = wheel;
	}
	
	

}
